static jobject generate_spoofed_attestation(JNIEnv* env, int algorithm, int security_level) {
    (void)algorithm;
    (void)security_level;
    
    jclass certificateFactoryClass = env->FindClass("java/security/cert/CertificateFactory");
    if (!certificateFactoryClass) {
        env->ExceptionClear();
        return NULL;
    }
    
    jmethodID getInstanceMethod = env->GetStaticMethodID(
        certificateFactoryClass, "getInstance", "(Ljava/lang/String;)Ljava/security/cert/CertificateFactory;");
    
    jstring x509Type = env->NewStringUTF("X.509");
    jobject certFactory = env->CallStaticObjectMethod(
        certificateFactoryClass, getInstanceMethod, x509Type);
    env->DeleteLocalRef(x509Type);
    
    if (!certFactory || env->ExceptionCheck()) {
        env->ExceptionClear();
        return NULL;
    }
    
    jmethodID generateCertMethod = env->GetMethodID(
        certificateFactoryClass, "generateCertificate",
        "(Ljava/io/InputStream;)Ljava/security/cert/Certificate;");
    
    jclass certificateClass = env->FindClass("java/security/cert/Certificate");
    jobjectArray certArray = env->NewObjectArray(2, certificateClass, NULL);
    
    jbyteArray deviceCertBytes = env->NewByteArray(sizeof(DEVICE_ATTESTATION_CERT_TEE));
    env->SetByteArrayRegion(deviceCertBytes, 0, sizeof(DEVICE_ATTESTATION_CERT_TEE),
                           (const jbyte*)DEVICE_ATTESTATION_CERT_TEE);
    
    jclass byteArrayInputStreamClass = env->FindClass("java/io/ByteArrayInputStream");
    jmethodID baosConstructor = env->GetMethodID(byteArrayInputStreamClass, "<init>", "([B)V");
    jobject deviceCertStream = env->NewObject(byteArrayInputStreamClass, baosConstructor, deviceCertBytes);
    
    jobject deviceCert = env->CallObjectMethod(certFactory, generateCertMethod, deviceCertStream);
    if (deviceCert && !env->ExceptionCheck()) {
        env->SetObjectArrayElement(certArray, 0, deviceCert);
    } else {
        env->ExceptionClear();
    }
    
    env->DeleteLocalRef(deviceCertStream);
    env->DeleteLocalRef(deviceCertBytes);
    
    jbyteArray rootCertBytes = env->NewByteArray(sizeof(GOOGLE_HW_ROOT_CA));
    env->SetByteArrayRegion(rootCertBytes, 0, sizeof(GOOGLE_HW_ROOT_CA),
                           (const jbyte*)GOOGLE_HW_ROOT_CA);
    
    jobject rootCertStream = env->NewObject(byteArrayInputStreamClass, baosConstructor, rootCertBytes);
    jobject rootCert = env->CallObjectMethod(certFactory, generateCertMethod, rootCertStream);
    if (rootCert && !env->ExceptionCheck()) {
        env->SetObjectArrayElement(certArray, 1, rootCert);
    } else {
        env->ExceptionClear();
    }
    
    env->DeleteLocalRef(rootCertStream);
    env->DeleteLocalRef(rootCertBytes);
    env->DeleteLocalRef(certFactory);
    
    LOGI("Generated spoofed attestation certificate chain");
    return certArray;
}

