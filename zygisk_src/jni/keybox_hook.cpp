static bool find_keygen_builder_class(JNIEnv* env) {
    g_keybox_state.keyGenParameterSpecBuilderClass = env->FindClass(
        "android/security/keystore/KeyGenParameterSpec$Builder");
    
    if (env->ExceptionCheck()) {
        env->ExceptionClear();
        LOGW("KeyGenParameterSpec.Builder not found (Android < 6.0?)");
        return false;
    }
    
    g_keybox_state.keyGenParameterSpecBuilderClass = 
        (jclass)env->NewGlobalRef(g_keybox_state.keyGenParameterSpecBuilderClass);
    
    g_keybox_state.setAttestationChallengeMethod = env->GetMethodID(
        g_keybox_state.keyGenParameterSpecBuilderClass,
        "setAttestationChallenge",
        "([B)Landroid/security/keystore/KeyGenParameterSpec$Builder;");
    
    if (env->ExceptionCheck()) {
        env->ExceptionClear();
        LOGW("setAttestationChallenge method not found (Android < 8.0?)");
        return false;
    }
    
    g_keybox_state.buildMethod = env->GetMethodID(
        g_keybox_state.keyGenParameterSpecBuilderClass,
        "build",
        "()Landroid/security/keystore/KeyGenParameterSpec;");
    
    return true;
}

